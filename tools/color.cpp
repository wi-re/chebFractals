const char *getName(Color col) {
  const auto &cols = getColors();
  for (auto v : cols)
    if (v.second == col)
      return v.first;
  return "unknown";
}
Color getColor(const char *col) {
  const auto &cols = getColors();
  for (auto v : cols)
    if (strcmp(v.first, col) == 0)
      return v.second;
  return Color::black;
}
Color getRandomColor() {
  const auto &cols = getColors();
  static std::random_device rd;  // Will be used to obtain a seed for the random number engine
  static std::mt19937 gen(rd()); // Standard mersenne_twister_engine seeded with rd()
  static std::uniform_int_distribution<int64_t> dis(0ull, cols.size());
  return cols[dis(gen)].second;
}

Color getHashedColor(std::string var) {
    const auto& cols = getColors();
    auto hashed = std::hash<std::string>{}(var);
    auto id = hashed % cols.size();
    return cols[id].second;
}
